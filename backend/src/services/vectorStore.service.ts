import { Pool } from 'pg';
import { getEmbedding } from './gemini.service';
import { PrismaClient } from '@prisma/client';

const prisma = new PrismaClient();

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
});

export const addKnowledge = async (
  content: string,
  source: string,
  category: string
): Promise<void> => {
  const chunk = await prisma.knowledgeChunk.create({
    data: { content, source, category },
  });

  const embedding = await getEmbedding(content);
  const vectorStr = `[${embedding.join(',')}]`;

  await pool.query(
    `INSERT INTO knowledge_embeddings (chunk_id, embedding)
     VALUES ($1::uuid, $2::vector)`,
    [chunk.id, vectorStr]
  );
};

export const searchSimilar = async (
  query: string,
  limit: number = 5
): Promise<string[]> => {
  const embedding = await getEmbedding(query);
  const vectorStr = `[${embedding.join(',')}]`;

  const result = await pool.query(
    `SELECT kc.content
     FROM knowledge_embeddings ke
     JOIN knowledge_chunks kc ON ke.chunk_id::text = kc.id::text
     ORDER BY ke.embedding <=> $1::vector
     LIMIT $2`,
    [vectorStr, limit]
  );

  return result.rows.map((row: { content: string }) => row.content);
};

